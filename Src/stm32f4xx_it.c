/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) && __HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_IDLE)){
		/* clear idle it flag avoid idle interrupt all the time */
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		
		/* clear DMA transfer complete flag */
		__HAL_DMA_DISABLE(huart1.hdmarx);
		
		uint16_t dma_data_counter = (uint16_t)(huart1.hdmarx->Instance->NDTR);
		/* handle dbus data uart1_rx_buff from DMA */
		if ((UART1_MAX_LEN - dma_data_counter) == UART1_RX_BUFFLEN)
		{
			receiver_RadioController_callback_handler();
		}
		if (dma_data_counter == UART1_MAX_LEN)
		{
			uart1_init();
		}
		/* restart dma transmission */
		__HAL_DMA_SET_COUNTER(huart1.hdmarx, UART1_MAX_LEN);
		__HAL_DMA_ENABLE(huart1.hdmarx);
	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

