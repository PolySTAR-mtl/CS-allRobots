extern uint32_t signOfLife_CAN1_tick;
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan){
	uint8_t buff[8];
	CAN_RxHeaderTypeDef rx_header;
	/* get device id ahead of time */
	rx_header.StdId = (CAN_RI0R_STID & hcan->Instance->sFIFOMailBox[CAN_RX_FIFO0].RIR) >> CAN_TI0R_STID_Pos;
	
  if(hcan->Instance == CAN1)
  {
		if ((rx_header.StdId >= 0x201) && (rx_header.StdId <=  0x211)) // judge the can id
		{
			signOfLife_CAN1_tick = HAL_GetTick();
			HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rx_header, buff); //receive can data
			can_motors_callback_handler(rx_header.StdId, buff);
		}
	}
}
